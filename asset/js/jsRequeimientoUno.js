var bandera = 1
function agregarBorde(){
    if (bandera == 1){
        document.getElementById('btnImg').style.border = '2px solid black';
        bandera = 2   
    }else if (bandera = 2){
        document.getElementById('btnImg').style.border = 'none';
        bandera = 1
    }
} 
